#!/bin/sh

# ================================================================
# Post-install Script
# ================================================================

set -e

# ----------------------------------------------------------------
# Clean up empty directory if necessary
# ----------------------------------------------------------------

cd /etc/ld.so.conf.d
[ -d '/codingcafe.conf.d' ] && echo 'include codingcafe.conf.d/*.conf' > /etc/ld.so.conf.d/codingcafe.conf || rm -f codingcafe.conf

# ----------------------------------------------------------------
# Flush ld cache
# ----------------------------------------------------------------

ldconfig
